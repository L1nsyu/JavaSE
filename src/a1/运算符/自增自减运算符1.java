package a1.运算符;

public class 自增自减运算符1 {
    public static void main(String[] args) {
        //++和--
        //++:变量的值加1
        //--:变量的值减1
        int a = 10;
        a++;
        System.out.println(a);//11
        ++a;
        System.out.println(a);//12
        a--;
        System.out.println(a);//11
        --a;
        System.out.println(a);//10
    }
}
