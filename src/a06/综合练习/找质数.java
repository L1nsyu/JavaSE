package a06.综合练习;

public class 找质数 {
    public static void main(String[] args) {
/*      一、错误原因分析
        以 100 为例，按照你的说法，需要检验 2~99 之间的数是否能整除 100，但实际上：
        100 的平方根是 10（即√100=10）。
        若 100 存在一个大于 10 的因数（例如 20），则必然存在一个小于 10 的对应因数（例如 5，因为 100=5×20）。
        因此，只要检验 2~10 之间的数是否能整除 100 即可，无需检验更大的数。
        二、关键原理：质数判定的 “平方根法则”
        核心结论：
        对于一个大于 1 的自然数n，如果n不是质数（即合数），则它一定存在一个不大于√n的因数。反之，若n在 2 到√n之间没有任何因数，则n是质数。
        三、举例验证
        判断 100 是否为质数
        √100=10，只需检验 2~10 之间的数：
        100 能被 2、4、5、10 整除（例如 100÷2=50），因此 100 是合数，无需检验 11~99 的数。
        判断 17 是否为质数
        √17≈4.12，只需检验 2~4 之间的数：
        17 不能被 2、3、4 整除，因此 17 是质数（无需检验 5~16 的数）。
        判断 29 是否为质数
        √29≈5.38，只需检验 2~5 之间的数：
        29 不能被 2、3、4、5 整除，因此 29 是质数。

        */

        //判断101~200之间有多少个素数,并打印所有素数
        int count = 0;
        for (int i = 101; i < 200; i++) {
            boolean flag = true;
            for (int j = 2; j < i; j++) {
                if (i % j == 0) {
                    flag = false;
                    break;
                }
            }
            if (flag) {
                count++;
                System.out.println("当前数字" + i + "是一个质数");
            } else {
                System.out.println("当前数字" + i + "不是一个质数");
            }
        }
        System.out.println(count);
    }
}
